    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try
        {
            String sid = jTextField1.getText();
            String fname = jTextField2.getText();
            String lname = jTextField3.getText();
            String number = jTextField5.getText();
            String email = jTextField4.getText();
            String uname = jTextField6.getText();
            String password = new String(jPasswordField1.getPassword());
            if(number.length()<10 || number.length()>10  )
            {
                JOptionPane.showMessageDialog(null,"Enter the correct number");
            }
            else
            {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/lib","root","abc");
                Statement st = con.createStatement();
                String query = "update student set fname='"+fname+"',lname='"+lname+"',num='"+number+"',email='"+email+"',uname='"+uname+"' ,pass='"+password+"' where sid = "+sid+";";
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(null,"RECORD UPDATE SUCCESSFULLY...");
                showde();
                
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

