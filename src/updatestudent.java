private void norepete()
{
   DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
   int r = model.getRowCount();
   for(int i = 0;i<r;i++)
   {
       model.removeRow(0);
   }
}
void getdata()
{
try
    {
        jTextField1.setText(rs.getString("sid"));
        jTextField2.setText(rs.getString("fname"));
        jTextField3.setText(rs.getString("lname"));
        jTextField4.setText(rs.getString("email"));
        jTextField5.setText(rs.getString("Num"));
        jTextField6.setText(rs.getString("Uname"));
        jPasswordField1.setText(rs.getString("pass"));
        
    }
catch(Exception e)
{
    JOptionPane.showMessageDialog(null,e.getMessage());
}
}
void showde()
{
DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        norepete();
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/lib","root","abc");
            Statement st = con.createStatement();
            String query = "select * from student";
            rs = st.executeQuery(query);
            while(rs.next())
            {
                String a =rs.getString(1);
                String b =rs.getString(2);
                String c =rs.getString(4);
                String d =rs.getString(5);
                String e =rs.getString(6);
                model.addRow(new Object[]{a,b,c,d,e});
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
}
